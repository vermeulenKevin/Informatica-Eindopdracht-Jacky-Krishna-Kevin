startscherm_image = pygame.image.load("images/startscherm.png")
startscherm_image = pygame.transform.scale(startscherm_image, (1440, 768))

def show_startscherm():
    for row in range(len(layout)):
        for col in range(len(layout[row])):
            # Bereken positie van de tegel
            x = (SCREENW - BOARDW) // 2 + col * CELLW
            y = (SCREENH - BOARDH) // 2 + row * CELLH

            if layout[row][col] == 1:
                screen.blit (startscherm_image, (x,y))
    
def coordinaten_start(layout):
    start_coordinaten = []

    for y in range(len(layout)):
        for x in range(len(layout[y])):
            if layout[y][x] == 2:
                start_coordinaten.append((x, y))   
    
    return start_coordinaten 

def coordinaten_rules(layout):
    rules_coordinaten = []

    for y in range(len(layout)):
        for x in range(len(layout[y])):
            if layout[y][x] == 3:
                rules_coordinaten.append((x, y))   
    
    return rules_coordinaten 

running = True
while running:  
    for event in pygame.event.get():
        if event.type == pygame.MOUSEBUTTONDOWN:
            mouse_pos = pygame.mouse.get_pos()
            cell_x = (mouse_pos[0] - ((SCREENW - BOARDW) // 2)) // CELLW
            cell_y = mouse_pos[1] // CELLH

            start_knop = coordinaten_start(layout)
            rules_knop = coordinaten_rules(layout)

            if (cell_x, cell_y) in start_knop:
                running = False
                pygame.quit()
                subprocess.run(["python", "main.py"])
            elif (cell_x, cell_y) in rules_knop:
                running = False
                pygame.quit()
                subprocess.run(["python", "spelregels.py"])
    show_startscherm()
    pygame.display.update()

pygame.quit()

