rules_image = pygame.image.load("images/regels2.png")
rules_image = pygame.transform.scale(rules_image, (1440, 768))

def show_startscherm():
    for row in range(len(layout)):
        for col in range(len(layout[row])):
            # Bereken positie van de tegel
            x = (SCREENW - BOARDW) // 2 + col * CELLW
            y = (SCREENH - BOARDH) // 2 + row * CELLH

            if layout[row][col] == 1:
                screen.blit (rules_image, (x,y))

running = True
while running:  
    for event in pygame.event.get():
        if event.type == pygame.MOUSEBUTTONDOWN:
            mouse_x, mouse_y = pygame.mouse.get_pos()
            print(f"Toets ingedrukt! Muispositie: ({mouse_x}, {mouse_y})")
            x, y = event.pos
            if start_knop.collidepoint(x, y):
                running = False
                pygame.quit()
                subprocess.run(["python", "main.py"])
    show_startscherm()
    pygame.display.update()

pygame.quit()

